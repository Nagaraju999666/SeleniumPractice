
public class WhileLoopConcept {

	public static void main(String[] args) {
     
		
		
		//while condition
		
		int i=0;
		while(i<10) {
			System.out.println(i);
			i=i+1;
		}
		
		
		System.out.println("***********************");
				
//			for loop
			
			for(int j=10; j<15; j++){
				System.out.println(j);
				
			}		
				System.out.println("----------------------------");
			
			
		for(int k=10; k>1; k--) {
				System.out.println(k);
			}

		}
	

}

